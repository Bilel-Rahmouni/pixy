async function isOk(id) {
  const res = await fetch(
    `https://images.unsplash.com/${id}?w=400&h=400&fit=crop&q=80`,
    { method: 'HEAD', redirect: 'follow' },
  )
  return res.ok
}

const src = readFileSync(new URL('../src/data/officePhotos.ts', import.meta.url), 'utf8')
let ids = [...src.matchAll(/"(photo-[^"]+)"/g)].map((m) => m[1])

const used = new Set(ids.filter((id) => !BAD.has(id)))
const validPool = []
for (const id of POOL) {
  if (used.has(id) || BAD.has(id)) continue
  if (await isOk(id)) validPool.push(id)
  used.add(id)
}

let pi = 0
ids = ids.map((id) => {
  if (!BAD.has(id)) return id
  const next = validPool[pi++]
  if (!next) throw new Error(`Not enough replacements for ${id}`)
  return next
})

const stillBad = []
for (const id of ids) {
  if (!(await isOk(id))) stillBad.push(id)
}

const unique = [...new Set(ids)]
console.log('Replaced', BAD.size, 'bad IDs')
console.log('Still bad:', stillBad.length, stillBad)
console.log('Total unique:', unique.length)

const out = `// Unique Unsplash office / workspace photos — no duplicates
export const officePhotoIds = ${JSON.stringify(unique, null, 2)} as const

export function officePhotoUrl(id: string) {
  return \`https://images.unsplash.com/\${id}?w=400&h=400&fit=crop&q=80\`
}
`

writeFileSync(new URL('../src/data/officePhotos.ts', import.meta.url), out)
